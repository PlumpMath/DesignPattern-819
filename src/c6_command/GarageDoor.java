package c6_command;

public class GarageDoor {

	public void up() {
		System.out.println("Door is Up");
	}
	
	public void down() {
		System.out.println("Door is Down");
	}
	
	public void stop() {
		System.out.println("Door is Stopped");
	}
	
	public void lightOn() {
		System.out.println("Light is On");
	}
	
	public void lightOff() {
		System.out.println("Light is Off");
	}
	
}
